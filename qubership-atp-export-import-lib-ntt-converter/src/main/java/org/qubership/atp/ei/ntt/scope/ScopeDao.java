package org.qubership.atp.ei.ntt.scope;

import java.util.ArrayList;
import java.util.List;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlAttribute;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlElementWrapper;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement(name = "scope-configuration")
@XmlAccessorType(XmlAccessType.FIELD)
public class ScopeDao {

    @XmlAttribute
    private String name;
    @XmlAttribute
    private String description;
    @XmlElementWrapper(name = "scope")
    @XmlElement(name = "item")
    private List<ScopeItemDao> scopeItems = new ArrayList<>();


    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getDescription() {
        return description;
    }

    public List<ScopeItemDao> getScopeItems() {
        return scopeItems;
    }
}
